package it.polimi.ingsw.Server.Model;

/**
 * this is every symbol that can be found on cards.
 * BLANK refers to "visible" corners that have no symbols
 * FULL refers to "hidden" corners
 */
public enum Symbol {
    MUSHROOM,
    WOLF,
    BUTTERFLY,
    LEAF,
    FEATHER,
    SCROLL,
    INK,
    BLANK,
    FULL
}
